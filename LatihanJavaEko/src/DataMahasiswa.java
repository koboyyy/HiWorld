public class DataMahasiswa{

    public static void main(String[] args) {
        //Deklarasi variabel
        int hasil;
        double ipk;
        char kelas;
        String kegiatan;

        //Inisialisasi variabel
        hasil = 10;
        ipk = 3.50;
        kelas = 'B';
        kegiatan = "Belajar Bahasa Pemrograman";

        System.out.println("Hasil Ujian saya adalah" + " " + hasil);
        System.out.println("Hasil IPK saya adalah" + " " + ipk);
        System.out.println("Hasil Kelas saya adalah" + " " + kelas);
        System.out.println("Hasil Kegiatan saya adalah" + " " + kegiatan);

    }

}
