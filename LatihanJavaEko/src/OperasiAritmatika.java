public class OperasiAritmatika {
    public static void main(String[] args) {
        //Deklarasi Variable
        int x, y, z;

        //inisialisasi variabel
        x = 10;
        y = 2;
        z = 4;

        //Menampilkan hasil
        System.out.println("Penjumlahan dari " + x + " + " + y + " + " + z + " = " + (x + y + z));
        System.out.println("Pembagian dari " + x + " / " + y + " = " + (x/y));
        System.out.println("Perkalian dari " + y + " * " + z + " = " + (y*z));

    }
}
