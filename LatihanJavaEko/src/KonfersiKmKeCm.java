public class KonfersiKmKeCm {
    // Deklarai konstanta
    public static final int CM = 100000;

    public static void main(String[] args) {
            
        int km = 8;

        int konfersi = km*CM;

        // Menampilkan hasil konversi
        System.out.println("Hasil " + km + "Km setelah di konfersi ke Cm adalah: " + konfersi + "Cm.");
        
    }
}
